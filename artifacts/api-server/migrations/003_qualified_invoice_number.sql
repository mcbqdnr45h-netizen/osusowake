-- 実行日: 2026-05-03
--
-- 背景: 2023年10月開始のインボイス制度において、 受領者が仕入税額控除を
--       受けるには領収書に「適格請求書発行事業者の登録番号」 (T + 13桁) が必須。
--       本マイグレーションで stores テーブルに任意フィールドを追加する。
-- ════════════════════════════════════════════════════════════════

ALTER TABLE stores
  ADD COLUMN IF NOT EXISTS qualified_invoice_number text;

COMMENT ON COLUMN stores.qualified_invoice_number IS
  '適格請求書発行事業者登録番号 (T + 13桁の数字、 例: T1234567890123)。 任意。';
